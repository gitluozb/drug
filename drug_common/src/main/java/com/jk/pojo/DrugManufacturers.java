package com.jk.pojo;

public class DrugManufacturers {
    //药品厂家id
    private Integer drugManufacturersId;
    //药品厂家名称
    private String drugManufacturersName;


    public Integer getDrugManufacturersId() {
        return drugManufacturersId;
    }

    public void setDrugManufacturersId(Integer drugManufacturersId) {
        this.drugManufacturersId = drugManufacturersId;
    }

    public String getDrugManufacturersName() {
        return drugManufacturersName;
    }

    public void setDrugManufacturersName(String drugManufacturersName) {
        this.drugManufacturersName = drugManufacturersName;
    }
}
